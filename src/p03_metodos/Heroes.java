package p03_metodos;

// Crear 5 métodos
// Cada métod0 mostrará el nombre de un superheroe. mostrarHeroeSpiderman()
// Mandar llamar a los métodos
public class Heroes {
    public static void main(String[] args) {
        mostrarHeroeBatichica();
        mostrarHeroeBatman();
        mostrarHeroeLaMole();
        mostrarHeroeSpiderman();
    }

    public static void mostrarHeroeSpiderman() {
        System.out.println("Spiderman");
    }

    public static void mostrarHeroeBatman() {
        System.out.println("Batman");
    }

    public static void mostrarHeroeLaMole() {
        System.out.println("La mole");
    }

    public static void mostrarHeroeBatichica() {
        System.out.println("Batichica");
    }
}
