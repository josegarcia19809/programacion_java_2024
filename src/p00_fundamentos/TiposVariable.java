package p00_fundamentos;

public class TiposVariable {
    public static void main(String[] args) {
        // Declaración de variables con distintos tipos
        int valor = 70;              // int
        String nombre = "José";      // String
        float calificacion = 8.5f;   // float (nota el sufijo f)
        boolean estaCasado = true;   // boolean

        // Impresiones
        System.out.println("nombre");       // imprime "nombre"
        System.out.println(nombre);         // imprime "José"
        System.out.println(valor);          // imprime 70
        System.out.println(calificacion);   // imprime 8.5
        System.out.println(estaCasado);     // imprime true
    }
}
